package com.oosphere.silverpeasmobile.vo;

public class ComponentVO {

  private String id;
  private String name;
  
  public ComponentVO(String id, String name) {
    this.id = id;
    this.name = name;
  }
  
  public String getId() {
    return id;
  }
  
  public String getName() {
    return name;
  }
  
}
